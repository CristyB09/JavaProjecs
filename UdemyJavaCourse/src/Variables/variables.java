package Variables;

//In Java there are 8 Primitive type of Variables;
// 'Int' integers are 32 value in java;
// 0ne byte can hold just 8 bits of data with can get up to only 127. Positive or Negative number;
public class variables {

    public static void main(String[] args) {

        int myNumber = 88;
        short myShort = 847;
        long myLong = 9797;

        double myDouble = 7.3243;
        float myFloat = 324.3f;

        char myChar = 'y';
        boolean myBoolean = false;

        byte myByte = 127;

        System.out.println(myNumber);
        System.out.println(myShort);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBoolean);
        System.out.println(myByte);

    }

}
